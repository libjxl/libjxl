// Regression test for the EXR extra-channel input-stride mismatch.
// Pre-patch: heap OOB read (ASan) + corrupted Z. Post-patch: clean + correct Z.
TEST(DecodeEXRTest, ExtraChannelWiderDisplayWindow) {
  if (!CanDecodeEXR()) GTEST_SKIP() << "EXR decoding not enabled";
  const int data_w = 2;
  const int img_w = 8;
  const int h = 4;

  PackedPixelFile ppf;
  const Span<const uint8_t> span(kCraftedExr, sizeof(kCraftedExr));
  ASSERT_TRUE(
      DecodeImageEXR(span, ColorHints(), &ppf, /*constraints=*/nullptr));

  EXPECT_EQ(ppf.info.xsize, static_cast<size_t>(img_w));
  EXPECT_EQ(ppf.info.ysize, static_cast<size_t>(h));
  ASSERT_EQ(ppf.frames.size(), 1u);
  ASSERT_EQ(ppf.frames[0].extra_channels.size(), 1u);

  const PackedImage& ec = ppf.frames[0].extra_channels[0];
  // The decoder copies the data/display overlap (x in [0, data_w)) into the
  // output; only those columns are defined, so we check exactly those.
  for (int y = 0; y < h; ++y) {
    const float* row = reinterpret_cast<const float*>(
        static_cast<const uint8_t*>(ec.pixels()) + ec.stride * y);
    for (int x = 0; x < data_w; ++x) {
      EXPECT_EQ(row[x], 100.f + 10.f * y + x)
          << "extra channel mismatch at (" << x << ", " << y << ")";
    }
  }
}

